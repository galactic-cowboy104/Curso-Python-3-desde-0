# Creando una tupla
vowels_tuple = ('a', 'e', 'i', 'o', 'u')
print(vowels_tuple)

# Accediendo a las posiciones [0:2] de la tupla
print(f'Posiciones [0:2]: {vowels_tuple[0:2]}')

# Accediendo a la posiciones [::2] de la tupla
print(f'Posiciones [::2]: {vowels_tuple[::2]}')

# Accediendo a la posiciones [-3:] de la tupla
print(f'Posiciones [-3:]: {vowels_tuple[-3:]}')
