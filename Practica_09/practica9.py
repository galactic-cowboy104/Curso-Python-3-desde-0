# SENTENCIAS CONDICIONALES MÚLTIPLES

# Pidiendo al usuario el valor a condicionar
num = int(input('¿Cuál es el número que deseas convertir?: '))

# Sentencia condicional múltiple
if num == 1:
    print('El número es: Uno')
elif num == 2:
    print('El número es: Dos')
elif num == 3:
    print('El número es: Tres')
elif num == 4:
    print('El número es: Cuatro')
elif num == 5:
    print('El número es: Cinco')
else:
    print('Este programa sólo puede convertir hasta el número 5.')
