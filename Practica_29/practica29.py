# ACCESO A ELEMENTOS DE UNA LISTA

# Creando una lista
marcas = ['Apple', 'Samsung', 'Xiaomi', 'Huawei', 'OPPO']

# Imprimendo el número de elementos de la lista
print(f'¿Cuántos elementos tiene la lista?: {len(marcas)}')

# Accediendo a la posición [2] de la lista
print(f'marcas[2]: {marcas[2]}')

# Accediendo a la posición [-4] de la lista
print(f'marcas[-4]: {marcas[-4]}')

# Accediendo a la posiciones [1:3] de la lista
print(f'marcas[1:3]: {marcas[1:3]}')

# Accediendo a la posiciones [3:] de la lista
print(f'marcas[3:]: {marcas[3:]}')

# Accediendo a la posiciones [:5] de la lista
print(f'marcas[:5]: {marcas[:5]}')

