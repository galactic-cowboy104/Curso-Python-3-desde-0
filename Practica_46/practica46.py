# DICCIONARIOS

# Creando un diccionario vacío
diccionario_vacio = {}
print(f'Diccionario vacío: {diccionario_vacio}')

# Creando un diccionario homogéneo
diccionario_edades = {'Juan': 32, 'Gerardo': 21, 'Luis': 25}
print(f'Diccionario de edades: {diccionario_edades}')

# Creando un diccionario heterogéneo
diccionario_info = {'name': 'Saúl', 'last_name': 'López', 'age': 18}
print(f'Diccionario de información: {diccionario_info}')

# Creando un diccionario anidado
diccionario_anidado = {'a': {'a': 1}, 'b': [0, 1, 2]}
print(f'Diccionario anidado: {diccionario_anidado}')