# Creando el diccionario
diccionario = {'a': 1, 'b': 2, 'c': 3}
print(f"El diccionario original es: {diccionario}")

# Eliminando el último item
item = diccionario.popitem()
print(f'El item eliminado es: {item}')

# Mostrando diccionario modificado
print(f'El diccionario modificado es: {diccionario}')