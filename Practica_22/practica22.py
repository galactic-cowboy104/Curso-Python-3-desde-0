# MÉTODO CAPITALIZE()

# Declarando la cadena
cadena = 'eL VIAjE eS la RecoMpEnSA'

# Mostrando el texto antes de aplicar capitalize()
print(f'Antes de capitalize(): {cadena}')

# Aplicando el método capitalize()
cadena = cadena.capitalize()

# Mostrando el texto después de aplicar capitalize()
print(f'Después de capitalize(): {cadena}')

