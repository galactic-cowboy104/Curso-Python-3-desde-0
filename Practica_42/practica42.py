# LISTAS ANIDADAS

# Creando la lista anidada
lista = [1, 'a', True, [1, 2, ['f', 'g', 'h']]]

# Mostrando algunos elementos de la lista anidada
print(f'El elemento de la posición [1] es: {lista[1]}')
print(f'El elemento de la posición [3][0] es: {lista[3][0]}')
print(f'El elemento de la posición [3][2][2] es: {lista[3][2][2]}')