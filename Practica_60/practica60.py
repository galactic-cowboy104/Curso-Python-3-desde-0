# TUPLAS

# Creando una tupla vacía
tupla_vacia = ()
print(f'Tupla vacía: {tupla_vacia}')

# Creando una tupla homogénea
tupla_homogenea = (3, 2)
print(f'Tupla homogénea: {tupla_homogenea}')

# Creando una tupla heterogénea 1
tupla_heterogenea1 = (4, True, 'Hola', 7.1)
print(f'Tupla heterogénea 1: {tupla_heterogenea1}')

# Creando una tupla heterogénea 2
tupla_heterogenea2 = ([1, 2, 3], {'uno': 1, 'dos': 2}, (8, 9))
print(f'Tupla heterogénea 2: {tupla_heterogenea2}')

# Creando una tupla de un elemento
tupla_de_un_elemento = (47, )
print(f'Tupla de un elemento: {tupla_de_un_elemento}')