# MATRICES CON LISTAS ANIDADAS

# Creando la matriz
matrix = [[1, 2, 3],
          [4, 5, 6],
          [7, 8, 9],]

# Mostrando la matriz completa
print(f'{matrix[0]}\n{matrix[1]}\n{matrix[2]}\n')

# Mostrando algunos elementos de la matriz
print(f'El elemento de la posición [0][0] es: {matrix[0][0]}')
print(f'El elemento de la posición [1][2] es: {matrix[1][2]}')
print(f'El elemento de la posición [2][1] es: {matrix[2][1]}')