# MÉTODOS CENTER(), LJUST() Y RJUST()

# Declarando cadena
cadena = 'MENÚ-RESTAURANT'

# Métodos con espacios
print(cadena.center(33))
print(cadena.ljust(33))
print(cadena.rjust(33))

print('')

# Métodos con caracter
print(cadena.center(33, '='))
print(cadena.ljust(33, '='))
print(cadena.rjust(33, '='))