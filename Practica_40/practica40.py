# MÉTODO SUM()

# Creando e imprimiendo lista
numeros = [1, 2, 3, 4]
print(f'La lista de números es: {numeros}')

# Mostrando la suma de los elementos de la lista
print(f'La suma de los elementos de la lista es: {sum(numeros)}')

# Mostrando la suma de los elementos de la lista más 10
print(f'La suma de los elementos de la lista más 10 es: {sum(numeros, 10)}')

# Mostrando la suma de los elementos de la lista menos 2
print(f'La suma de los elementos de la lista menos 2 es: {sum(numeros, -2)}')

# Mostrando la suma de los elementos de la lista más True
print(f'La suma de los elementos de la lista más True es: {sum(numeros, True)}')