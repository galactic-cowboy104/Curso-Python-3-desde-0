# OPERADORES ARITMÉTICOS

# Suma
numero_uno = 3
numero_dos = 4

resultado = numero_uno + numero_dos
print('El resultado de la suma es: ' + str(resultado))

# Resta
numero_uno = 9
numero_dos = 5

resultado = numero_uno - numero_dos
print('El resultado de la resta es: ' + str(resultado))

# Multiplicación
numero_uno = 7
numero_dos = 6

resultado = numero_uno * numero_dos
print('El resultado de la multiplicación es: ' + str(resultado))

# División
numero_uno = 32
numero_dos = 8

resultado = numero_uno / numero_dos
print('El resultado de la división es: ' + str(resultado))

# Potenciación
numero_uno = 2
numero_dos = 10

resultado = numero_uno ** numero_dos
print('El resultado de la potenciación es: ' + str(resultado))

# Módulo
numero_uno = 30
numero_dos = 4

resultado = numero_uno % numero_dos
print('El resultado del módulo es: ' + str(resultado))

# División entera
numero_uno = 65
numero_dos = 9

resultado = numero_uno // numero_dos
